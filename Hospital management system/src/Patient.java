    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
           String pno = lblpno.getText();
    
    try {
        // Show confirmation dialog and store user response
        int confirm = JOptionPane.showConfirmDialog(
            null, 
            "Do you really want to delete this patient?", 
            "Select", 
            JOptionPane.YES_NO_OPTION
        );
        
        // Check if the user selected YES
        if (confirm == JOptionPane.YES_OPTION) {
            pst = con.prepareStatement("delete from patient where patientno = ?");
            pst.setString(1, pno);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Patient Deleted.");
            
            // Reset fields
            AutoID();
            txtpname.setText("");
            txtphone.setText("");
            txtadress.setText("");
            txtpname.requestFocus();
            patient_table();

            jButton1.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Deletion Cancelled.");
        }
    } catch (SQLException ex) {
        Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton3ActionPerformed

